//Java Program to find quotient and remainder

public class QuotientRemainder {
	public static void main(String[] args) {
		int a = 13;
		int b = 4;
		double rem = a%b;
		double qut = a/b;
		System.out.println("Quotient of "+a+" and "+b+" is "+qut );
		System.out.println("Remainder of "+a+" and "+b+" is "+rem );

	}
}
